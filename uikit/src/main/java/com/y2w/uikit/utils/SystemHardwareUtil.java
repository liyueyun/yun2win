package com.y2w.uikit.utils;

import android.os.Build;

/**
 * 系统和硬件制造方面的工具类
 * @author Administrator
 *
 */
public class SystemHardwareUtil {

	/**
	 * 是否是华为系统
	 * 在拍照或者录像时，华为UI 3.0系统拍照后按sdk获取照片不成功，需要另外处理
	 * 
	 * 其中还无法确认是华为UI3.0系统还是华为硬件问题，即其他手机root华为3.0系统后效果不知会如何，或者华为手机root为其他系统
	 * 
	 * @return
	 */
	public static boolean isHuaweiPhoto(){
		return "Huawei".equals(Build.BRAND);
	}
}
